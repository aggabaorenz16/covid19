fetch('https://corona.lmao.ninja/v2/countries/Philippines')
.then((response) => {
    return response.json();
})
.then((data)=>{
document.getElementById('ph-Actived').innerHTML = data.active;
document.getElementById('ph-deaths').innerHTML = data.deaths;
document.getElementById('ph-recovered').innerHTML = data.recovered;
document.getElementById('ph-Case').innerHTML = data.cases;
document.getElementById('ph-tdcases').innerHTML = data.todayCases;
})
